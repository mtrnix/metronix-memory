"""
Benchmark schemas — questions, claims, and API request models.

Adapted from metronix-benchmarker for integration into Metronix Core.
QEDDocument is a dataclass used internally by DocumentSampler.
BenchmarkQuestion, QuestionAttributes, Claim, ClaimSource are Pydantic models
for API serialization. GenerateRequest and RunTestsRequest are API request schemas.
"""

from __future__ import annotations

from dataclasses import dataclass

from pydantic import BaseModel, Field, field_validator

# ---------------------------------------------------------------------------
# Dataclass: document format expected by BenchmarkQED
# ---------------------------------------------------------------------------


@dataclass
class QEDDocument:
    """Document in the format expected by BenchmarkQED."""

    source_id: str
    title: str
    text: str
    source_type: str
    url: str


# ---------------------------------------------------------------------------
# Pydantic models: claims and question attributes
# ---------------------------------------------------------------------------


class ClaimSource(BaseModel):
    """Source reference for a claim."""

    source_id: str = Field(..., description="Source document ID")
    text: str = Field(..., description="Text excerpt from source")
    url: str | None = Field(None, description="URL to source document")
    source_type: str | None = Field(None, description="Source type (jira/confluence)")
    title: str | None = Field(None, description="Source document title")


class Claim(BaseModel):
    """Single claim (assertion) produced by BenchmarkQED."""

    statement: str = Field(..., description="Claim statement text")
    sources: list[ClaimSource] = Field(
        default_factory=list, description="Sources backing the claim"
    )
    score: int = Field(..., ge=0, le=100, description="Claim score (0-100)")
    source_ids: list[str] = Field(default_factory=list, description="List of source document IDs")


class QuestionAttributes(BaseModel):
    """Attributes attached to a BenchmarkQED question."""

    input_question: str = Field(..., description="Original input question")
    period: str | None = Field(None, description="Time period")
    location: str | None = Field(None, description="Location")
    named_entities: list[str] = Field(default_factory=list, description="Named entities")
    abstract_categories: list[str] = Field(default_factory=list, description="Abstract categories")
    background_information: str | None = Field(None, description="Background information")
    reference_coverage: float = Field(..., ge=0.0, le=1.0, description="Reference coverage")
    relevant_reference_count: int = Field(..., ge=0, description="Number of relevant references")
    reference_count: int = Field(..., ge=0, description="Total number of references")
    min_reference_similarity: float = Field(..., description="Minimum reference similarity")
    max_reference_similarity: float = Field(..., description="Maximum reference similarity")
    mean_reference_similarity: float = Field(..., description="Mean reference similarity")
    intra_inter_similarity_ratio: float = Field(..., description="Intra/inter similarity ratio")
    claim_count: int = Field(..., ge=0, description="Number of claims")
    claims: list[Claim] = Field(default_factory=list, description="List of claims")
    is_representative: bool = Field(
        default=True, description="Whether the question is representative"
    )


# ---------------------------------------------------------------------------
# Pydantic model: benchmark question
# ---------------------------------------------------------------------------


class BenchmarkQuestion(BaseModel):
    """Question generated by BenchmarkQED."""

    id: str = Field(..., description="Unique question ID")
    text: str = Field(..., min_length=1, description="Question text")
    question_type: str = Field(..., description="Question type (data_local / data_global)")
    references: list[str] = Field(default_factory=list, description="Reference source texts")
    attributes: QuestionAttributes = Field(..., description="Question attributes with claims")

    @field_validator("text")
    @classmethod
    def validate_text_not_empty(cls, v: str) -> str:
        """Ensure question text is not blank."""
        if not v.strip():
            raise ValueError("Question text cannot be empty")
        return v.strip()


# ---------------------------------------------------------------------------
# API request schemas
# ---------------------------------------------------------------------------


class GenerateRequest(BaseModel):
    """Request body for POST /generate."""

    workspace_id: str = Field(..., description="Target workspace ID")
    connection_id: str = Field(..., description="DB connection ID to sample documents from")
    num_questions: int = Field(default=10, ge=1, description="Number of questions to generate")
    num_clusters: int | None = Field(
        None, ge=1, description="Number of clusters for question generation"
    )


class RunTestsRequest(BaseModel):
    """Request body for POST /run-tests."""

    workspace_id: str = Field(..., description="Target workspace ID")
    benchmark_set_id: str = Field(..., description="Benchmark set to test against")
    name: str = Field(..., min_length=1, description="Test run name")
    description: str | None = Field(None, description="Optional test run description")


class SaveBenchmarkRequest(BaseModel):
    """Request body for POST /benchmarks (save/update/create)."""

    id: str | None = Field(None, description="Benchmark ID (if provided, upsert)")
    name: str = Field(..., min_length=1, description="Benchmark set name")
    connection_id: str = Field(..., description="Connection ID this benchmark belongs to")
    description: str | None = Field(None, description="Optional description")
    questions: list[dict] = Field(..., description="List of question dicts")
    tokens_used: int = Field(default=0, ge=0, description="Tokens used during generation")
